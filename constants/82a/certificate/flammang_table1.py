def get_table():
    """Return list of (c_j, ascending integer coeff list)."""
    out = []
    for c, desc in _TABLE_DESCENDING:
        # assert integrality
        for a in desc:
            assert isinstance(a, int), f"non-integer coeff {a!r}"
        asc = list(reversed(desc))
        out.append((float(c), asc))
    return out


