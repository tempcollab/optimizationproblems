toks = re.findall(r'\d{5}', WORDS_RAW)
words = [tuple(int(c) for c in tk) for tk in toks]
print("parsed words:", len(words))
assert len(words) == 367, f"expected 367, got {len(words)}"

# all digits in 0..6
for w in words:
    for c in w:
        assert 0 <= c <= 6, f"bad digit {c} in {w}"

# distinct?
print("distinct words:", len(set(words)))
assert len(set(words)) == 367, "words not distinct!"

def cyclic_dist(a, b):
    d = abs(a - b)
    return min(d, 7 - d)

def confusable_word(u, v):
    # adjacent (confusable) iff EVERY coordinate confusable (cyclic dist <= 1)
    return all(cyclic_dist(u[k], v[k]) <= 1 for k in range(5))

# Independence: NO pair confusable
bad = 0
first_bad = None
for i in range(len(words)):
    for j in range(i+1, len(words)):
        if confusable_word(words[i], words[j]):
            bad += 1
            if first_bad is None:
                first_bad = (i, j, words[i], words[j])
print("confusable (bad) pairs:", bad)
if first_bad:
    print("FIRST BAD:", first_bad)
    sys.exit(1)
print("RESULT: independent set of size 367 in C_7^5 VERIFIED.")
print("367^(1/5) =", 367**(1/5))
